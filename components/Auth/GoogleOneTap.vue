<template>
  <Teleport to="body">
    <Transition name="slide-down">
      <div
        v-if="isVisible"
        class="fixed top-4 right-4 md:top-8 md:right-8 w-[calc(100%-2rem)] md:w-96 bg-white rounded-xl shadow-[0_8px_30px_rgb(0,0,0,0.12)] border border-gray-100 z-[100] overflow-hidden"
      >
        <!-- Header with Close -->
        <div class="p-4 flex items-start justify-between">
          <div class="flex gap-3">
            <div class="w-10 h-10 flex-shrink-0 bg-gray-50 rounded-full flex items-center justify-center border border-gray-100">
              <svg class="w-6 h-6" viewBox="0 0 24 24">
                <path
                  fill="#4285F4"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
                <path
                  fill="#34A853"
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                />
                <path
                  fill="#FBBC05"
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                />
                <path
                  fill="#EA4335"
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                />
              </svg>
            </div>
            <div>
              <h3 class="text-sm font-semibold text-gray-900">Quick sign in</h3>
              <p class="text-xs text-gray-500 mt-0.5">Continue to Modeh with Google</p>
            </div>
          </div>
          <button @click="dismiss" class="text-gray-400 hover:text-gray-600 transition-colors p-1">
            <Icon name="heroicons:x-mark" class="w-5 h-5" />
          </button>
        </div>

        <!-- Body -->
        <div class="px-4 pb-4">
          <button
            @click="loginWithGoogle"
            :disabled="loading"
            class="w-full flex items-center justify-center gap-3 bg-white border border-gray-300 rounded-lg px-4 py-2.5 text-sm font-medium text-gray-700 hover:bg-gray-50 hover:border-gray-400 transition-all shadow-sm"
          >
            <template v-if="!loading">
              <svg class="w-5 h-5" viewBox="0 0 24 24">
                <path
                  fill="#4285F4"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
              </svg>
              Sign in with Google
            </template>
            <Icon v-else name="heroicons:arrow-path" class="w-5 h-5 animate-spin text-gray-400" />
          </button>
          
          <div class="mt-3 text-center">
            <NuxtLink to="/login" @click="dismiss" class="text-xs text-brand-600 hover:text-brand-700 font-medium">
              Use email address instead
            </NuxtLink>
          </div>
        </div>
      </div>
    </Transition>
  </Teleport>
</template>

<script setup>
import { ref, onMounted } from 'vue'
import { useAnalytics } from '~/composables/useAnalytics'

const { status, signIn } = useAuth()
const { trackLogin } = useAnalytics()
const isVisible = ref(false)
const loading = ref(false)

const dismiss = () => {
  isVisible.value = false
  // Store dismissal in session storage to not show it again in this session
  if (typeof window !== 'undefined') {
    sessionStorage.setItem('google-one-tap-dismissed', 'true')
  }
}

const loginWithGoogle = async () => {
  loading.value = true
  try {
    const result = await signIn('google', { callbackUrl: '/' })
    // Track successful login
    if (result?.ok) {
      trackLogin('google')
    }
  } catch (e) {
    console.error('Google Sign-In Error:', e)
    loading.value = false
  }
}

onMounted(() => {
  // Show after a short delay if user is not authenticated and hasn't dismissed it
  if (status.value === 'unauthenticated') {
    const dismissed = sessionStorage.getItem('google-one-tap-dismissed')
    if (!dismissed) {
      setTimeout(() => {
        isVisible.value = true
      }, 3000)
    }
  }
})
</script>

